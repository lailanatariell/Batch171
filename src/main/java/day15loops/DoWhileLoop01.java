package day15loops;

public class DoWhileLoop01 {
   /*
     baslangiv degeri
     while(loop calisma kurali){
     calisacak kodlar

     arttirma/azaltme
     }

    */
   public static void main(String[] args) {
       for (int i = 3; i < 7; i++) {
           System.out.println(i+" ");

       }

       int j=3;
       while (j<7){
           System.out.print(j+" ");
           j++;
       }
       System.out.println();
   }
}
